    public void PreencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"Código","Descrição","Qtde-Estoque","Valor Unitário","Valor Total"};
        conex.conexao();
        conex.executaSql(Sql);
        try{
            conex.rs.first();
            do{
                dados.add(new Object[]{conex.rs.getInt("cod_produtos"),conex.rs.getString("descricao_produtos"),conex.rs.getInt("qtde_produtos"),conex.rs.getDouble("valoruni_produtos"),conex.rs.getDouble("valor_total")});
            }while(conex.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,"Erro ao preencher ArrayList"+ex);
        }
        ModeloTabelaProduto modelo = new ModeloTabelaProduto (dados, colunas);
        jTableProdutos.setModel (modelo);
        jTableProdutos.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTableProdutos.getColumnModel().getColumn(0).setResizable(false);
        
        jTableProdutos.getColumnModel().getColumn(1).setPreferredWidth(262);
        jTableProdutos.getColumnModel().getColumn(1).setResizable(false);
        
        jTableProdutos.getColumnModel().getColumn(2).setPreferredWidth(90);
        jTableProdutos.getColumnModel().getColumn(2).setResizable(false);
        
        jTableProdutos.getColumnModel().getColumn(3).setPreferredWidth(90);
        jTableProdutos.getColumnModel().getColumn(3).setResizable(false);
        
        jTableProdutos.getColumnModel().getColumn(4).setPreferredWidth(90);
        jTableProdutos.getColumnModel().getColumn(4).setResizable(false);
        
        jTableProdutos.getTableHeader().setReorderingAllowed(false);
        jTableProdutos.setAutoResizeMode(jTableProdutos.AUTO_RESIZE_OFF);
        jTableProdutos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
    }
