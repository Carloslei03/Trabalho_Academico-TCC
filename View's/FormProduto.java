    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if(flag==1){
        mod.setDescricao(jTextFieldDescricao.getText());
        mod.setValorunitario(Double.parseDouble(jTextFieldPreco.getText()));
        mod.setQtdeestoque(Integer.parseInt(jTextFieldQtdeEstoque.getText()));
        mod.setValortotal(mod.valortotal);
        control.Salvar(mod);
        jTextFieldDescricao.setText(""); //Limpa o campo NOME
        jTextFieldPreco.setText("");//Limpa o campo EMAIL
        jTextFieldQtdeEstoque.setText("");//Limpa o campo TELEFONE
        jTextFieldDescricao.setEnabled(false);
        jTextFieldPreco.setEnabled(false);
        jTextFieldQtdeEstoque.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        jButtonCancelar.setEnabled(false);
        }else{//Setando dentro dos modelos o que o usuario digitou para alteração
            mod.setCodigo((Integer.parseInt(jFormattedTextFieldCodigo.getText())));
            mod.setDescricao(jTextFieldDescricao.getText());
            mod.setValorunitario(Double.parseDouble(jTextFieldPreco.getText()));
            mod.setQtdeestoque(Integer.parseInt(jTextFieldQtdeEstoque.getText()));
            control.Atualizar(mod);//Ira enviar para o editar o mod alterado, o modo editar recebe e faz o update
            
            jFormattedTextFieldCodigo.setText("");
            jTextFieldDescricao.setText("");
            jTextFieldPreco.setText("");
            jTextFieldQtdeEstoque.setText("");
            jTextFieldDescricao.setEnabled(false);
            jTextFieldPreco.setEnabled(false);
            jTextFieldQtdeEstoque.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            
            jButtonNovo.setEnabled(true);
            jButtonCancelar.setEnabled(false);
        }    
    }//GEN-LAST:event_jButtonSalvarActionPerformed

